public class XESResource {
    private String name;
    private int count;
    private int counts;
    private int counte;

    public XESResource(String name) {
        this.name = name;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getCounte() {
        return counte;
    }

    public void setCounte(int counte) {
        this.counte = counte;
    }

    public int getCounts() {
        return counts;
    }

    public void setCounts(int counts) {
        this.counts = counts;
    }
    
    
    
}
